function or() {
  local re=`join "|" "$@"`
  grep -i -E "($re)"
}

#-------------------------------------------------

function nor() {
  local re=`join "|" "$@"`
  grep -i -E -v "($re)"
}

#-------------------------------------------------

function and() {
  local result=`join "\" | grep -i \"" "$@"`

  eval "grep -i -E \"$result\""
}

